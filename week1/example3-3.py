# 숫자 변수에 여러가지 연산자를 사용해보자
num1 = 12
num2 = 5
print("num1 + num2 =", num1 + num2)     # 덧셈
print("num1 - num2 =", num1 - num2)     # 뺄셈
print("num1 * num2 =", num1 * num2)     # 곱셈
print("num1 / num2 =", num1 / num2)     # 나눗셈
print("num1 % num2 =", num1 % num2)     # 나머지
print("num1 // num2 =", num1 // num2)   # 몫

