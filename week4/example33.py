# 리스트 데이터를 사용해봅시다.

# 문자열 리스트
friends = ["홍길동", "김민석", "박서연"]
print("변수 friends 의 타입은", type(friends))

# 숫자 리스트
ages = [15, 14, 16]
print("변수 ages 의 타입은", type(ages))

# 리스트 데이터 하나씩 가져와봅시다.
print("첫번째 친구의 이름은", friends[0], "이고 나이는", ages[0])
print("두번째 친구의 이름은", friends[1], "이고 나이는", ages[1])
print("세번째 친구의 이름은", friends[2], "이고 나이는", ages[2])
